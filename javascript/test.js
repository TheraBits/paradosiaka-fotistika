function renderProducts(categ, lang) {
  let content = "";
  console.log("content631", content);

  categ.forEach((product) => {
    let productHTML = `<div class="polaroid">
       <img id="${product.name[lang]}" class="fotistika-img" alt="${product.alt[lang]}" src="${product.imageURL}">
       <div class="container">
       <p class="polaroid-codename">${product.name[lang]}</p>
       <p>${product.description[lang]}</p>
       </div>
     </div>`;
    content += productHTML;
    console.log("content643", content);
    // Update the innerHTML of the fanaria-section element with the new content
    document.getElementById("fanaria-section").innerHTML = content;
  });
}

